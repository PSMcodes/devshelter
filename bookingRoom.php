        function updateCounterDisplay(counterId, value) {
            document.getElementById(counterId).textContent = value;
            console.log(value);
        }

        function incrementCounter(counterId) {
            let counterElement = document.getElementById(counterId);
            let count = parseInt(counterElement.textContent);
            updateCounterDisplay(counterId, count + 1);
            checkAndUpdateCounters();
        }

        function decrementCounter(counterId) {
            let counterElement = document.getElementById(counterId);
            let count = parseInt(counterElement.textContent);
            if (count > 0) {
                updateCounterDisplay(counterId, count - 1);
            }
            checkAndUpdateCounters();
        }

        function decrementRoomCounter() {
            let roomCounterElement = document.getElementById("roomCounter");
            let roomCount = parseInt(roomCounterElement.textContent);
            let totalGuests = parseInt(
                document.getElementById("guestCounterDisplay").textContent
            );
            let requiredRooms = Math.ceil(totalGuests / 4);
            if (roomCount > requiredRooms) {
                updateCounterDisplay("roomCounter", roomCount - 1);
                updateCounterDisplay("roomCounterDisplay", roomCount - 1);
            }
        }

        function checkAndUpdateCounters() {
            let adultCount = parseInt(
                document.getElementById("adultCounter").textContent
            );
            let childCount = parseInt(
                document.getElementById("childCounter").textContent
            );

            let totalGuests = adultCount + childCount;
            updateCounterDisplay("guestCounterDisplay", totalGuests);

            let roomCounterElement = document.getElementById("roomCounter");
            let roomCount = Math.ceil(totalGuests / 3); // Calculate the required number of rooms
            roomCount = Math.max(roomCount, 1); // Ensure at least one room

            updateCounterDisplay("roomCounter", roomCount);
            updateCounterDisplay("roomCounterDisplay", roomCount);
        }

        let InquiryformBtn = document.getElementById("submitBtn");

        InquiryformBtn.addEventListener("click", (e) => {
            e.preventDefault();
            let stayLocation = document.getElementsByName("location")[0].value;
            let checkInDate = document.getElementById("date0").value;
            let checkOutDate = document.getElementById("date1").value;
            let noOfGuest = document.getElementById(
                "guestCounterDisplay"
            ).innerHTML;
            let noOfRooms = document.getElementById("roomCounterDisplay").innerHTML;
            window.location.href = `room.html?location=${stayLocation}&checkIn=${checkInDate}&checkOut=${checkOutDate}&guest=${noOfGuest}&rooms=${noOfRooms}`;
        });
